// 5. Напишите программу, которая на вход принимает одно число (N), а на выходе показывает все целые числа в промежутке от -N до N.

//4 -> "-4, -3, -2, -1, 0, 1, 2, 3, 4"
//2 -> " -2, -1, 0, 1, 2"

using static System.Console;
WriteLine("Введите число a ");
int a = int.Parse(ReadLine()!);
int n = -a;
while (n <= a)
{
    Write($"{n} ");
    n = n + 1;
}
